create trigger check_insert before insert on products
for each row
begin
    if(isnull(coalesce(NEW.name, NEW.description))) then
		signal sqlstate '45000' set message_text = 'error insert 45000';
    end if;
end//

create trigger check_update before update on products
for each row
begin
    if(isnull(coalesce(NEW.name, NEW.description))) then
		signal sqlstate '45000' set message_text = 'error update 45000';
    end if;
end//
